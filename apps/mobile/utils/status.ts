// Response Utility Function
export function createResponse<T>({
  status,
  message,
  code,
  data = null,
}: {
  status: StatusCode;
  message: string;
  code?: MessageCodes;
  data: T;
}) {
  return new Response(
    JSON.stringify({
      status,
      message,
      code,
      data,
    }),
    { status },
  );
}
